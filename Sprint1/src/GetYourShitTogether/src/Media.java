
abstract class Media {
	
	String title;
	String genre;
	float rating;
	int releaseDate;
	String plot;
	int id;
	String studio;
	
	public String getTitle()
	{
		return this.title;
	}
	
	public String getGenre()
	{
		return this.title;
	}
	
	public float getRating()
	{
		return this.rating;
	}
	
	public int getReleaseDate()
	{
		return this.releaseDate;
	}
	
	public String getPlot()
	{
		return this.plot;
	}
	
	public int getID()
	{
		return this.id;
	}
	
	public String getStudio()
	{
		return this.studio;
	}
	
	
}
